package com.slxt.rs.vo;

public class Wp {
	private String wpNo;
	private String wpName;
	private String wpCount;
	private String wpUnit;
	public String getWpNo() {
		return wpNo;
	}
	public void setWpNo(String wpNo) {
		this.wpNo = wpNo;
	}
	public String getWpName() {
		return wpName;
	}
	public void setWpName(String wpName) {
		this.wpName = wpName;
	}
	public String getWpCount() {
		return wpCount;
	}
	public void setWpCount(String wpCount) {
		this.wpCount = wpCount;
	}
	public String getWpUnit() {
		return wpUnit;
	}
	public void setWpUnit(String wpUnit) {
		this.wpUnit = wpUnit;
	}
	
}
