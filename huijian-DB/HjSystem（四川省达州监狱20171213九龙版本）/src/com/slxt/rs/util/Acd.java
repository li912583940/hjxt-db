package com.slxt.rs.util;

public class Acd {
	private int acdNo;
	private String acdVoc;
	private long ShowID;
	public int getAcdNo() {
		return acdNo;
	}
	public void setAcdNo(int acdNo) {
		this.acdNo = acdNo;
	}
	public String getAcdVoc() {
		return acdVoc;
	}
	public void setAcdVoc(String acdVoc) {
		this.acdVoc = acdVoc;
	}
	public long getShowID() {
		return ShowID;
	}
	public void setShowID(long showID) {
		ShowID = showID;
	}
	
	
}
